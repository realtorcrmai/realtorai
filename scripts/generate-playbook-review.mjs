#!/usr/bin/env node
/**
 * Monthly Playbook Review Generator — Self-Learning Phase 3
 *
 * Reads: compliance log (full history), lessons-learned.md, weekly-insights.md,
 *        agent-playbook.md (line count), playbook modules (line counts)
 * Outputs: .claude/playbook/monthly-review-YYYY-MM.md
 *
 * Proposes: rules to ADD, REMOVE, UPDATE, or TRIM.
 * Human reviews and approves before any playbook edit.
 *
 * Usage: node scripts/generate-playbook-review.mjs
 */

import { readFileSync, writeFileSync, readdirSync, existsSync, statSync } from "fs";
import { join } from "path";

const ROOT = process.cwd();
const NOW = new Date();
const MONTH = `${NOW.getFullYear()}-${String(NOW.getMonth() + 1).padStart(2, "0")}`;

// --- Helpers ---

function readIfExists(path) {
  return existsSync(path) ? readFileSync(path, "utf-8") : "";
}

function lineCount(path) {
  return existsSync(path) ? readFileSync(path, "utf-8").split("\n").length : 0;
}

function parseComplianceLog() {
  const content = readIfExists(join(ROOT, ".claude/compliance-log.md"));
  const lines = content.split("\n");
  const entries = [];

  for (const line of lines) {
    if (!line.startsWith("|") || line.includes("---") || line.includes("Date")) continue;
    const cols = line.split("|").map((c) => c.trim()).filter(Boolean);
    if (cols.length < 5) continue;

    const [date, developer, summary, type, followed, completed, skipped, notes] = cols;
    if (!date.match(/^\d{4}-\d{2}/)) continue;

    entries.push({
      date, developer, summary, type,
      passed: followed?.includes("✅") || followed?.includes("YES") || followed?.includes("PASS"),
      skipped: skipped || "—",
      notes: notes || "",
    });
  }
  return entries;
}

function parseLessons() {
  const content = readIfExists(join(ROOT, ".claude/playbook/lessons-learned.md"));
  return content.split("\n")
    .filter((l) => l.startsWith("| 20"))
    .map((l) => {
      const cols = l.split("|").map((c) => c.trim()).filter(Boolean);
      return { date: cols[0], type: cols[1], lesson: cols[2] };
    });
}

// --- Analysis ---

function generateReview() {
  const entries = parseComplianceLog();
  const lessons = parseLessons();
  const r = []; // report lines

  r.push(`# Playbook Monthly Review — ${MONTH}`);
  r.push("");
  r.push(`> Auto-generated by \`scripts/generate-playbook-review.mjs\``);
  r.push(`> **Action required:** Review each proposal. Apply approved changes manually.`);
  r.push("");

  // --- Overview ---
  r.push("## Overview");
  r.push("");
  r.push(`- **Period:** ${entries.length > 0 ? entries[0].date : "N/A"} to ${entries.length > 0 ? entries[entries.length - 1].date : "N/A"}`);
  r.push(`- **Tasks:** ${entries.length}`);

  const passed = entries.filter((e) => e.passed).length;
  const rate = entries.length > 0 ? ((passed / entries.length) * 100).toFixed(0) : "N/A";
  r.push(`- **Compliance rate:** ${rate}% (${passed}/${entries.length})`);
  r.push(`- **Lessons captured:** ${lessons.length}`);
  r.push("");

  // --- File sizes (ETH Zurich: smaller is better) ---
  r.push("## Playbook Size Audit (HC-16: <550 lines per file)");
  r.push("");

  const playbookFiles = [
    { name: "agent-playbook.md (core)", path: ".claude/agent-playbook.md" },
    { name: "task-playbooks.md", path: ".claude/playbook/task-playbooks.md" },
    { name: "governance.md", path: ".claude/playbook/governance.md" },
    { name: "validation.md", path: ".claude/playbook/validation.md" },
    { name: "operations.md", path: ".claude/playbook/operations.md" },
    { name: "compliance.md", path: ".claude/playbook/compliance.md" },
    { name: "model-chaining.md", path: ".claude/playbook/model-chaining.md" },
  ];

  let totalLines = 0;
  r.push("| File | Lines | Status |");
  r.push("|------|-------|--------|");
  for (const f of playbookFiles) {
    const lines = lineCount(join(ROOT, f.path));
    totalLines += lines;
    const status = lines > 550 ? "OVER LIMIT" : lines > 500 ? "Near limit" : "OK";
    r.push(`| ${f.name} | ${lines} | ${status} |`);
  }
  r.push(`| **Total** | **${totalLines}** | ${totalLines > 3000 ? "Consider trimming" : "Healthy"} |`);
  r.push("");

  // --- Propose ADDING ---
  r.push("## Propose ADDING");
  r.push("");

  const lessonCounts = {};
  for (const l of lessons) {
    const key = l.lesson?.slice(0, 50) || "unknown";
    lessonCounts[key] = (lessonCounts[key] || 0) + 1;
  }
  const recurring = Object.entries(lessonCounts).filter(([, count]) => count >= 2);

  if (recurring.length > 0) {
    for (const [lesson, count] of recurring) {
      r.push(`- **Promote to HC rule:** "${lesson}" (occurred ${count}x)`);
    }
  } else {
    r.push("- No recurring lessons detected yet. Accumulate more data.");
  }

  // Check if any skipped phase appears 3+ times
  const skipCounts = {};
  for (const e of entries) {
    if (!e.skipped || e.skipped === "—" || e.skipped === "None") continue;
    for (const phase of e.skipped.split(",").map((p) => p.trim()).filter(Boolean)) {
      skipCounts[phase] = (skipCounts[phase] || 0) + 1;
    }
  }
  const frequentSkips = Object.entries(skipCounts).filter(([, c]) => c >= 3);
  for (const [phase, count] of frequentSkips) {
    r.push(`- **Enforce phase "${phase}"** — skipped ${count}x. Add hook enforcement or make it mandatory with a gate.`);
  }
  r.push("");

  // --- Propose REMOVING ---
  r.push("## Propose REMOVING");
  r.push("");

  // Check HC rules — any that were NEVER violated?
  // We can't automatically detect this, but we can flag it for human review
  r.push("- Review HC-1 through HC-17: Are any rules NEVER violated? Consider removing to reduce context size.");
  r.push("- Review each playbook module: sections with 0 references in compliance log may be dead weight.");
  r.push("");

  // --- Propose UPDATING ---
  r.push("## Propose UPDATING");
  r.push("");

  // Task type distribution → playbook tuning
  const typeCounts = {};
  for (const e of entries) {
    const baseType = e.type?.split(":")[0] || "unknown";
    typeCounts[baseType] = (typeCounts[baseType] || 0) + 1;
  }
  const sortedTypes = Object.entries(typeCounts).sort((a, b) => b[1] - a[1]);

  if (sortedTypes.length > 0) {
    const [topType, topCount] = sortedTypes[0];
    const pct = ((topCount / entries.length) * 100).toFixed(0);
    if (parseInt(pct) > 40) {
      r.push(`- **${topType}** is ${pct}% of all tasks. Ensure its playbook section has the most detail and examples.`);
    }
  }

  // Developer-specific patterns
  const devStats = {};
  for (const e of entries) {
    if (!devStats[e.developer]) devStats[e.developer] = { total: 0, passed: 0 };
    devStats[e.developer].total++;
    if (e.passed) devStats[e.developer].passed++;
  }
  for (const [dev, stats] of Object.entries(devStats)) {
    const devRate = ((stats.passed / stats.total) * 100).toFixed(0);
    if (parseInt(devRate) < 80 && stats.total >= 3) {
      r.push(`- **${dev}** compliance at ${devRate}% (${stats.passed}/${stats.total}). Schedule process review.`);
    }
  }
  r.push("");

  // --- Propose TRIMMING ---
  r.push("## Propose TRIMMING");
  r.push("");
  r.push("Per ETH Zurich research: long context files degrade agent performance. Target: playbook gets SMALLER over time.");
  r.push("");

  const claudeMdLines = lineCount(join(ROOT, "CLAUDE.md"));
  if (claudeMdLines > 450) {
    r.push(`- **CLAUDE.md** is ${claudeMdLines} lines. Audit for inferable content (obvious project structure, standard Next.js patterns).`);
  }
  if (totalLines > 2500) {
    r.push(`- **Total playbook** is ${totalLines} lines across ${playbookFiles.length} files. Look for redundancy between modules.`);
  }

  r.push("");
  r.push("---");
  r.push("");
  r.push("*Review complete. Apply approved changes manually, then commit with type `DOCS:runbook`.*");

  return r.join("\n");
}

// --- Main ---
console.log(`📋 Generating monthly playbook review for ${MONTH}\n`);

const report = generateReview();
const outputPath = join(ROOT, `.claude/playbook/monthly-review-${MONTH}.md`);
writeFileSync(outputPath, report);

console.log(report);
console.log(`\n✅ Report saved to ${outputPath}`);
