async function timedFetch(url, options = {}) {
  const start = Date.now();
  try {
    const res = await fetch(url, { signal: AbortSignal.timeout(15000), ...options });
    const elapsed = Date.now() - start;
    return { status: res.status, elapsed, ok: res.ok, data: await res.text().catch(() => null) };
  } catch (e) {
    return { status: 0, elapsed: Date.now() - start, ok: false, data: null, error: e.message };
  }
}

