async function cleanup() {
  console.log("\n\x1b[1m=== CLEANUP ===\x1b[0m\n");

  // Find all test contacts by phone prefix
  const { data: testContacts } = await supabase.from("contacts")
    .select("id,name,phone").like("phone", `${TEST_PHONE_PREFIX}%`);

  if (!testContacts?.length) {
    console.log("  No test contacts to clean up.");
    return;
  }

  const contactIds = testContacts.map((c) => c.id);
  console.log(`  Found ${contactIds.length} test contacts to clean up.`);

  // Delete newsletter_events for test contacts
  const { error: evtErr } = await supabase.from("newsletter_events")
    .delete().in("contact_id", contactIds);
  if (evtErr) console.log(`  Warning: newsletter_events cleanup: ${evtErr.message}`);
  else console.log("  Cleaned newsletter_events.");

  // Delete newsletters for test contacts
  const { error: nlErr } = await supabase.from("newsletters")
    .delete().in("contact_id", contactIds);
  if (nlErr) console.log(`  Warning: newsletters cleanup: ${nlErr.message}`);
  else console.log("  Cleaned newsletters.");

  // Delete contact_journeys for test contacts
  const { error: cjErr } = await supabase.from("contact_journeys")
    .delete().in("contact_id", contactIds);
  if (cjErr) console.log(`  Warning: contact_journeys cleanup: ${cjErr.message}`);
  else console.log("  Cleaned contact_journeys.");

  // Delete workflow_enrollments for test contacts
  const { error: weErr } = await supabase.from("workflow_enrollments")
    .delete().in("contact_id", contactIds);
  if (weErr) console.log(`  Warning: workflow_enrollments cleanup: ${weErr.message}`);
  else console.log("  Cleaned workflow_enrollments.");

  // Delete test workflows
  const { error: wfErr } = await supabase.from("workflows")
    .delete().like("slug", "e2e_%");
  if (wfErr) console.log(`  Warning: workflows cleanup: ${wfErr.message}`);
  else console.log("  Cleaned test workflows.");

  // Delete test contacts
  const { error: cErr } = await supabase.from("contacts")
    .delete().in("id", contactIds);
  if (cErr) console.log(`  Warning: contacts cleanup: ${cErr.message}`);
  else console.log(`  Cleaned ${contactIds.length} test contacts.`);

  console.log("  Cleanup complete.");
}

