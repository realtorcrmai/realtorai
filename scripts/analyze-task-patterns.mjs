#!/usr/bin/env node
/**
 * Weekly Task Pattern Analyzer — Self-Learning Playbook Phase 2
 *
 * Reads: .claude/task-archive/*.json + .claude/compliance-log.md + .claude/playbook/lessons-learned.md
 * Outputs: .claude/playbook/weekly-insights.md
 *
 * Usage: node scripts/analyze-task-patterns.mjs [--days 7]
 */

import { readFileSync, writeFileSync, readdirSync, existsSync } from "fs";
import { join } from "path";

const ROOT = process.cwd();
const DAYS = parseInt(process.argv.find((a) => a.startsWith("--days="))?.split("=")[1] ?? "7");
const NOW = Date.now();
const CUTOFF = NOW - DAYS * 24 * 60 * 60 * 1000;

// --- Read compliance log ---
function parseComplianceLog() {
  const logPath = join(ROOT, ".claude/compliance-log.md");
  if (!existsSync(logPath)) return [];

  const lines = readFileSync(logPath, "utf-8").split("\n");
  const entries = [];

  for (const line of lines) {
    if (!line.startsWith("|") || line.includes("---") || line.includes("Date")) continue;
    const cols = line.split("|").map((c) => c.trim()).filter(Boolean);
    if (cols.length < 5) continue;

    const [date, developer, summary, type, followed, phasesCompleted, phasesSkipped, notes] = cols;
    const entryDate = new Date(date);
    if (isNaN(entryDate.getTime()) || entryDate.getTime() < CUTOFF) continue;

    entries.push({
      date,
      developer: developer || "unknown",
      summary: summary || "",
      type: type || "unknown",
      passed: followed?.includes("✅") || followed?.includes("YES") || followed?.includes("PASS"),
      phasesCompleted: phasesCompleted || "",
      phasesSkipped: phasesSkipped || "",
      notes: notes || "",
    });
  }

  return entries;
}

// --- Read task archive ---
function parseTaskArchive() {
  const archiveDir = join(ROOT, ".claude/task-archive");
  if (!existsSync(archiveDir)) return [];

  const files = readdirSync(archiveDir).filter((f) => f.endsWith(".json"));
  const tasks = [];

  for (const file of files) {
    try {
      const content = readFileSync(join(archiveDir, file), "utf-8");
      const task = JSON.parse(content);
      tasks.push({ file, ...task });
    } catch {
      // Skip malformed files
    }
  }

  return tasks;
}

// --- Read lessons ---
function parseLessons() {
  const lessonsPath = join(ROOT, ".claude/playbook/lessons-learned.md");
  if (!existsSync(lessonsPath)) return [];

  const lines = readFileSync(lessonsPath, "utf-8").split("\n");
  return lines.filter((l) => l.startsWith("| 20")).map((l) => {
    const cols = l.split("|").map((c) => c.trim()).filter(Boolean);
    return { date: cols[0], type: cols[1], lesson: cols[2] };
  });
}

// --- Analysis ---
function analyze(entries, tasks, lessons) {
  const report = [];
  const weekStart = new Date(CUTOFF).toISOString().split("T")[0];
  const weekEnd = new Date().toISOString().split("T")[0];

  report.push(`# Weekly Insights — ${weekStart} to ${weekEnd}`);
  report.push("");
  report.push(`> Auto-generated by \`scripts/analyze-task-patterns.mjs\``);
  report.push(`> Review and promote recurring patterns to playbook rules.`);
  report.push("");

  // --- Task counts by type ---
  report.push("## Tasks by Type");
  report.push("");
  const typeCounts = {};
  for (const e of entries) {
    typeCounts[e.type] = (typeCounts[e.type] || 0) + 1;
  }
  const sortedTypes = Object.entries(typeCounts).sort((a, b) => b[1] - a[1]);
  report.push("| Type | Count | % |");
  report.push("|------|-------|---|");
  for (const [type, count] of sortedTypes) {
    report.push(`| ${type} | ${count} | ${((count / entries.length) * 100).toFixed(0)}% |`);
  }
  report.push(`| **Total** | **${entries.length}** | |`);
  report.push("");

  // --- Compliance rate ---
  report.push("## Compliance Rate");
  report.push("");
  const passed = entries.filter((e) => e.passed).length;
  const failed = entries.filter((e) => !e.passed).length;
  const rate = entries.length > 0 ? ((passed / entries.length) * 100).toFixed(0) : "N/A";
  report.push(`- **PASS:** ${passed} | **FAIL:** ${failed} | **Rate:** ${rate}%`);

  if (failed > 0) {
    report.push("");
    report.push("**Failed tasks:**");
    for (const e of entries.filter((e) => !e.passed)) {
      report.push(`- ${e.date} | ${e.type} | ${e.summary.slice(0, 60)} — Skipped: ${e.phasesSkipped}`);
    }
  }
  report.push("");

  // --- Most skipped phases ---
  report.push("## Most Skipped Phases");
  report.push("");
  const skipCounts = {};
  for (const e of entries) {
    if (!e.phasesSkipped || e.phasesSkipped === "—" || e.phasesSkipped === "None") continue;
    const phases = e.phasesSkipped.split(",").map((p) => p.trim()).filter(Boolean);
    for (const phase of phases) {
      skipCounts[phase] = (skipCounts[phase] || 0) + 1;
    }
  }
  const sortedSkips = Object.entries(skipCounts).sort((a, b) => b[1] - a[1]);
  if (sortedSkips.length > 0) {
    report.push("| Phase | Times Skipped | Recommendation |");
    report.push("|-------|--------------|----------------|");
    for (const [phase, count] of sortedSkips.slice(0, 5)) {
      let rec = "Monitor";
      if (count >= 3) rec = "**Add hook enforcement or make mandatory**";
      else if (count >= 2) rec = "Review if phase is needed for this task type";
      report.push(`| ${phase} | ${count} | ${rec} |`);
    }
  } else {
    report.push("No phases skipped this week.");
  }
  report.push("");

  // --- Developer breakdown ---
  report.push("## Developer Activity");
  report.push("");
  const devCounts = {};
  for (const e of entries) {
    if (!devCounts[e.developer]) devCounts[e.developer] = { total: 0, passed: 0 };
    devCounts[e.developer].total++;
    if (e.passed) devCounts[e.developer].passed++;
  }
  report.push("| Developer | Tasks | Compliance Rate |");
  report.push("|-----------|-------|-----------------|");
  for (const [dev, stats] of Object.entries(devCounts)) {
    const devRate = ((stats.passed / stats.total) * 100).toFixed(0);
    report.push(`| ${dev} | ${stats.total} | ${devRate}% |`);
  }
  report.push("");

  // --- Lessons this week ---
  report.push("## Lessons Captured");
  report.push("");
  if (lessons.length > 0) {
    for (const l of lessons.slice(-10)) {
      report.push(`- **${l.type}:** ${l.lesson}`);
    }
  } else {
    report.push("No lessons captured this week.");
  }
  report.push("");

  // --- Recommendations ---
  report.push("## Recommendations");
  report.push("");
  const recs = [];

  // High failure rate
  if (entries.length > 0 && (failed / entries.length) > 0.2) {
    recs.push(`Compliance rate ${rate}% is below 80% threshold. Review failed tasks and identify root cause.`);
  }

  // Repeated skips
  for (const [phase, count] of sortedSkips) {
    if (count >= 3) {
      recs.push(`Phase "${phase}" skipped ${count}x. Consider: (a) make it hook-enforced, or (b) remove it if not needed for that task type.`);
    }
  }

  // Single task type dominance
  if (sortedTypes.length > 0 && sortedTypes[0][1] / entries.length > 0.5) {
    recs.push(`${sortedTypes[0][0]} represents ${((sortedTypes[0][1] / entries.length) * 100).toFixed(0)}% of all tasks. Ensure playbook is well-tuned for this type.`);
  }

  // No tasks
  if (entries.length === 0) {
    recs.push("No compliance log entries this week. Either no work was done or entries are missing.");
  }

  if (recs.length === 0) {
    recs.push("No action items this week. Playbook compliance is healthy.");
  }

  for (const r of recs) {
    report.push(`- ${r}`);
  }

  return report.join("\n");
}

// --- Main ---
const entries = parseComplianceLog();
const tasks = parseTaskArchive();
const lessons = parseLessons();

console.log(`📊 Analyzing ${entries.length} compliance entries, ${tasks.length} archived tasks, ${lessons.length} lessons (last ${DAYS} days)\n`);

const report = analyze(entries, tasks, lessons);

const outputPath = join(ROOT, ".claude/playbook/weekly-insights.md");
writeFileSync(outputPath, report);
console.log(report);
console.log(`\n✅ Report saved to ${outputPath}`);
