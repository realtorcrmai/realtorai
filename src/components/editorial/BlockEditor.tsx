// ── Main component ────────────────────────────────────────────────────────────

interface BlockEditorProps {
  block: EditorBlock
  onUpdate: (blockId: string, content: Partial<EditorBlockContent>) => void
  onToggleLock: (blockId: string) => void
  isGenerating: boolean
}

export function BlockEditor({ block, onUpdate, onToggleLock, isGenerating }: BlockEditorProps) {
  function handleChange(update: Partial<EditorBlockContent>) {
    onUpdate(block.id, update)
  }

  // Determine if any content has been set (for "AI generated" indicator)
  // We consider it AI-generated if the block has content but is not locked
  const hasContent = (() => {
    const c = block.content as unknown as Record<string, unknown>
    return Object.values(c).some((v) => v !== null && v !== '' && v !== undefined)
  })()

  /** Load the most recent just_sold transaction and auto-populate this block */
  async function handleLoadTransaction() {
    const { listTransactions } = await import('@/actions/editorial')
    const result = await listTransactions()
    if (!result.data || result.data.length === 0) return

    // Prefer a just_sold transaction; fall back to whatever exists
    const tx = result.data.find(t => t.transaction_type === 'just_sold') ?? result.data[0]

    onUpdate(block.id, {
      address: tx.address,
      sale_price: tx.sale_price ? tx.sale_price / 100 : 0,
      list_price: tx.list_price / 100,
      days_on_market: tx.days_on_market ?? 0,
      commentary: tx.story ?? null,
      sold_date: tx.sold_at ?? '',
    } as Partial<EditorBlockContent>)
  }

  return (
    <div className="flex flex-col">
      {/* Lock banner */}
      <LockBanner
        isLocked={block.is_locked}
        isAiGenerated={hasContent && !block.is_locked}
        onToggleLock={() => onToggleLock(block.id)}
        disabled={isGenerating}
      />

      {/* Field editors by type */}
      {block.type === 'hero' && (
        <HeroEditor
          content={block.content}
          onChange={(u) => handleChange(u)}
          disabled={isGenerating}
        />
      )}
      {block.type === 'just_sold' && (
        <JustSoldEditor
          content={block.content}
          onChange={(u) => handleChange(u)}
          disabled={isGenerating}
          onLoadTransaction={handleLoadTransaction}
        />
      )}
      {block.type === 'market_commentary' && (
        <MarketCommentaryEditor
          content={block.content}
          onChange={(u) => handleChange(u)}
          disabled={isGenerating}
        />
      )}
      {block.type === 'rate_watch' && (
        <RateWatchEditor
          content={block.content}
          onChange={(u) => handleChange(u)}
          disabled={isGenerating}
        />
      )}
      {block.type === 'local_intel' && (
        <LocalIntelEditor
          content={block.content}
          onChange={(u) => handleChange(u)}
          disabled={isGenerating}
        />
      )}
      {block.type === 'neighborhood_spotlight' && (
        <NeighborhoodSpotlightEditor
          content={block.content}
          onChange={(u) => handleChange(u)}
          disabled={isGenerating}
        />
      )}
      {block.type === 'quick_tip' && (
        <QuickTipEditor
          content={block.content}
          onChange={(u) => handleChange(u)}
          disabled={isGenerating}
        />
      )}
      {block.type === 'agent_note' && (
        <AgentNoteEditor
          content={block.content}
          onChange={(u) => handleChange(u)}
          disabled={isGenerating}
        />
      )}
      {block.type === 'cta' && (
        <CtaEditor
          content={block.content}
          onChange={(u) => handleChange(u)}
          disabled={isGenerating}
        />
      )}
      {block.type === 'divider' && <DividerEditor />}
    </div>
  )
}
