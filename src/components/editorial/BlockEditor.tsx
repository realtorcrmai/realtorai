function FieldGroup({ children, className }: { children: React.ReactNode; className?: string }) {
  return <div className={['flex flex-col gap-1.5', className].filter(Boolean).join(' ')}>{children}</div>
}

function CharCount({ value, max }: { value: string | null | undefined; max: number }) {
  const len = value?.length ?? 0
  const over = len > max
  return (
    <span className={['text-xs tabular-nums', over ? 'text-destructive font-medium' : 'text-muted-foreground'].join(' ')}>
      {len}/{max}
    </span>
  )
}

function WordCount({ value }: { value: string | null | undefined }) {
  const words = value?.trim() ? value.trim().split(/\s+/).length : 0
  return (
    <span className="text-xs text-muted-foreground tabular-nums">{words} words</span>
  )
}

interface LockBannerProps {
  isLocked: boolean
  isAiGenerated: boolean
  onToggleLock: () => void
  disabled: boolean
}

function LockBanner({ isLocked, isAiGenerated, onToggleLock, disabled }: LockBannerProps) {
  if (isLocked) {
    return (
      <div className="flex items-center justify-between rounded-lg bg-[#f5c26b]/10 border border-[#f5c26b]/30 px-3 py-2 mb-4">
        <div className="flex items-center gap-2">
          <Badge variant="warning" className="text-xs">✏️ Manually edited</Badge>
          <span className="text-xs text-muted-foreground">Won&apos;t be regenerated by AI</span>
        </div>
        <Button
          variant="ghost"
          size="xs"
          onClick={onToggleLock}
          disabled={disabled}
          aria-label="Unlock this block for AI regeneration"
        >
          🔓 Unlock
        </Button>
      </div>
    )
  }

  if (isAiGenerated) {
    return (
      <div className="flex items-center justify-between rounded-lg bg-muted/40 border border-border px-3 py-2 mb-4">
        <div className="flex items-center gap-2">
          <span className="text-xs text-muted-foreground">🤖 AI generated</span>
        </div>
        <Button
          variant="ghost"
          size="xs"
          onClick={onToggleLock}
          disabled={disabled}
          aria-label="Lock this block to prevent AI from overwriting your edits"
        >
          🔒 Lock edits
        </Button>
      </div>
    )
  }

  return (
    <div className="flex items-center justify-end mb-4">
      <Button
        variant="ghost"
        size="xs"
        onClick={onToggleLock}
        disabled={disabled}
        aria-label="Lock this block to prevent AI from overwriting your edits"
      >
        🔒 Lock edits
      </Button>
    </div>
  )
}

